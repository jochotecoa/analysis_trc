##### Metadata #####

if (exists("cts")) {
  timepoints_vec <- rep(DEFAULT_TIMEPOINTS, each = 3)
  coldata <- data.frame(row.names = colnames(cts), 
                        timepoints = as.factor(timepoints_vec))

  ##### DESeq2 Pipeline #####

  dds <- DESeqDataSetFromMatrix(countData = cts, 
                                colData = coldata, 
                                design = ~ timepoints)

  if (setSizeFactorToOne) {
    sizeFactors(dds) <- rep(1, ncol(dds))
  }

  dds <- DESeq(dds)

  # Results and Normalization
  norm_data <- counts(dds, normalized = TRUE)
  
  output_dir <- file.path('results', DEFAULT_COMP, 'DESeq2', project_name)
  setOrCreatewd(output_dir)
  
  write.table(norm_data, file = "normalized_counts.txt", sep = "\t", quote = FALSE)

  # PCA Plot
  rld <- vst(dds)
  pca_plot <- plotPCA(rld, intgroup = "timepoints")
  ggsave("pca_plot.png", plot = pca_plot)

  # Extract DEGs for all timepoints vs baseline (002)
  res_names <- resultsNames(dds)[-1]
  all_degs <- data.frame(ensembl_gene_id = rownames(dds))
  
  for (name in res_names) {
    res <- results(dds, name = name)
    res_df <- as.data.frame(res) %>% tibble::rownames_to_column('ensembl_gene_id')
    colnames(res_df)[-1] <- paste(colnames(res_df)[-1], name, sep = "_")
    all_degs <- merge(all_degs, res_df[, c("ensembl_gene_id", paste0("padj_", name))], by = "ensembl_gene_id", all = TRUE)
    
    # Save individual result
    write.table(res, file = paste0("DE_results_", name, ".txt"), sep = "\t", quote = FALSE)
  }
  
  write.table(all_degs, file = "summary_DEGs_padj.txt", sep = "\t", quote = FALSE, row.names = FALSE)
  
  print("DESeq2 Analysis completed successfully.")
}