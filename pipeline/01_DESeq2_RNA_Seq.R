#' Get counts for the specified project
getCts <- function(project_type) {
  if (grepl('TRC', project_type)) {
    # TRC Data logic
    if (dir.exists(TRC_COUNTS_PATH)) {
      setwd(TRC_COUNTS_PATH)
      # Looking for the specific counts file generated by trcToCounts.R
      counts_file = list.files(pattern = 'counts_TRC_UNTR_.*\.tsv')
      if (length(counts_file) > 0) {
        quant_file = read.table(counts_file[1], header = TRUE)
        return(column_to_rownames(quant_file, 'ensembl_gene_id'))
      }
    }
    
    # Fallback to merging TRCscore files if counts file not found
    if (dir.exists(TRC_OUTPUT_PATH)) {
      # This part usually requires finding the latest timestamped directory
      latest_dir = list.dirs(TRC_OUTPUT_PATH, recursive = FALSE) %>% tail(1)
      if (length(latest_dir) > 0) {
        setwd(latest_dir)
        quant_file = mergeFiles(files_patt = 'TRCscore', row_names = TRUE, all = TRUE)
      } else {
        stop("TRC output directory empty or not found.")
      }
    }
    col_quant = 'TRC_'
    transcript_id = 'ensembl_transcript_id'
    
  } else if (grepl('Salmon', project_type)) {
    # Salmon Data logic
    if (dir.exists(SALMON_INPUT_PATH)) {
      setwd(SALMON_INPUT_PATH)
      col_quant = if (grepl('counts', project_type)) 'NumReads' else 'TPM'
      quant_file = mergeFiles(files_patt = 'quant.sf', by_col = 'Name', all = TRUE)
      transcript_id = 'ensembl_transcript_id_version'
    } else {
      stop("Salmon input path not found: ", SALMON_INPUT_PATH)
    }
  } else {
    stop("Unknown project type: ", project_type)
  }

  # Map transcripts to genes and aggregate
  quant_file = quant_file %>% filter(grepl(quant_file[, 1], pattern = 'ENST'))
  mart = openMart2018()
  
  tx2gene = getBM(attributes = c(transcript_id, 'ensembl_gene_id', 'transcript_biotype'), 
                  filters = transcript_id, values = quant_file[, 1], mart = mart)
  
  # Filter for protein coding
  tx2gene = tx2gene[tx2gene$transcript_biotype == 'protein_coding', ]
  
  quant_file_genes = merge(quant_file, tx2gene, by.x = colnames(quant_file)[1], by.y = transcript_id)
  
  cts_data = quant_file_genes %>% dplyr::select(contains(col_quant))
  cts_data = apply(cts_data, 2, round) %>% as.data.frame()
  
  # Aggregate by gene
  cts_final = aggregate(cts_data, by = list(ensembl_gene_id = quant_file_genes$ensembl_gene_id), FUN = sum) %>%
    tibble::column_to_rownames('ensembl_gene_id')
    
  return(cts_final)
}

